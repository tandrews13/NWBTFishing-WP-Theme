    <h3>Measuring format:</h3>
    <p>All participants will be provided an official NWBT bump board. It will be each team’s responsibility to see to it that the bump board is returned at year's end and in the condition it was when it was provided to the participants. If the board is lost, stolen, misplaced or damaged, it will be the team’s responsibility to pay for the board.</p>
    <p>On the bump board the belly of the fish must be facing the person measuring, the mouth of the fish must touch the bump plate on the rulers left side (head to the left) mouth closed, and the tail is extended back to its maximum length on the RIGHT end of the ruler.</p>
    <p>While the fish is on the bump board, a “scoring shot” MUST be taken. Both the HEAD and TAIL of the fish must be included and show clearly. Again this photo must be of the LEFT side of the fish ONLY. A photo that does not show the overall length of the fish, including the head and tail and of the fish’s left side WILL NOT BE SCORED.</p>
    <p>Next you MUST take your “hero shot”, this photo must be taken showing the RIGHT side of the fish.  </p>
    <p>If BOTH the “scoring shot” and “hero shot” are not taken for each scoring fish that fish WILL NOT BE COUNTED.</p>
    <h3>BIG FISH WEEKLY BONUS-No purchase necessary.  $10 per boat</h3>
    <p>This will be a completely separate pot from the weekly payout. 100% of the entries for Big Fish will be paid out each week to the winning team. Weekly Big Fish: fish can either be photographed on ruler and with angler, or can be verified with another boat.  All fish over 20 inches are encouraged to be released. </p>
    <p>Boats must be at the access at 9 pm, or be subject to penalty.  A penalty of ½LB per 5 minutes late will be deducted from that night’s total bag, up to 15 minutes, after 15 minutes that boat will be DQ’d for the evening. Time will be based off GPS time.</p>
    <p>At the conclusion of the night participants are welcomed to come back to the bait shop where we can clean and fry fish while talking about the evening. Once results are tallied they will be posted at the bait shop as well as on our Facebook page.</p>
